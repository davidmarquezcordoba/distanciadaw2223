/**
 *
 * Esta función realiza la suma de dos números que se pasan por parámetros y devuelve el resultado de la misma.
 *
 * @param int $a El primer número.
 * @param int $b El segundo número.
 * @return int La suma de los dos números.
 *
 */

function sumar($a, $b) {
    return $a + $b;
}

/**
 *
 * Esta función realiza la resta de dos números que se pasan por parámetros y devuelve el resultado de la misma.
 *
 * @param int $a El primer número.
 * @param int $b El segundo número.
 * @return int La resta de los dos números.
 * 
 */

function restar($a, $b) {
    return $a - $b;
}


